/**
 * カードタイプ定義
 * Card type definitions
 */
export enum CardType {
  SORCERER = 'SORCERER',        // 呪術師
  TECHNIQUE = 'TECHNIQUE',      // 術式
  DOMAIN = 'DOMAIN',            // 領域
  BINDING_VOW = 'BINDING_VOW',  // 縛り
  CURSED_OBJECT = 'CURSED_OBJECT', // 呪具
}

/**
 * 派閥
 */
export enum Faction {
  TOKYO = 'TOKYO',
  KYOTO = 'KYOTO',
  SPECIAL_GRADE = 'SPECIAL_GRADE',
  CURSED_SPIRIT = 'CURSED_SPIRIT',
  NEUTRAL = 'NEUTRAL',
}

/**
 * カード効果タイプ
 */
export enum EffectType {
  DAMAGE = 'DAMAGE',
  HEAL = 'HEAL',
  DRAW = 'DRAW',
  ENERGY_GAIN = 'ENERGY_GAIN',
  SHIELD = 'SHIELD',
  BUFF_ATTACK = 'BUFF_ATTACK',
  DEBUFF = 'DEBUFF',
  DOUBLE_DAMAGE = 'DOUBLE_DAMAGE',
  AOE_DAMAGE = 'AOE_DAMAGE',
  SELF_DAMAGE = 'SELF_DAMAGE',
}

/**
 * カード効果
 */
export interface CardEffect {
  type: EffectType;
  value: number;
  description: string;
  descriptionJa: string;
}

/**
 * カード定義インターフェース
 */
export interface CardData {
  id: string;
  nameJa: string;
  nameFurigana: string;
  nameEn: string;
  type: CardType;
  faction: Faction;
  cost: number;
  power: number;
  defense: number;
  hp: number;
  effect: CardEffect;
  flavorTextJa: string;
  isLegendary?: boolean;
  selfDamage?: number;
}

/**
 * ヒーローパワー定義
 */
export interface HeroPower {
  nameJa: string;
  nameFurigana: string;
  cost: number;
  descriptionJa: string;
  effect: CardEffect;
}

